package oopIntroOdev;

public class Movie {
	int ID;
	String name;
	String detail;
	String director;
	int length;

	Movie(int ID, String name, String detail, String director, int length) {
		this.ID = ID;
		this.name = name;
		this.detail = detail;
		this.director = director;
		this.length = length;
	}
}
